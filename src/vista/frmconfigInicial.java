    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtnombre.getText().length() > 4 && txtPassword.getText().length() > 4) {
            String estado;
            if (cmbEstado.getText().toString().equals("Activo")) {
                estado = "SI";
            } else {
                estado = "NO";
            }
            String nomUs = txtnombre.getText();
            String contras = txtPassword.getText();
            String tipC = cbmtipoC.getText();
            String estC = estado;
            UC.guardarUsuario(nomUs, contras, tipC, estC);
            txtnombre.setText("");
            txtPassword.setText("");
            this.dispose();
            ControlUsuario cu = new ControlUsuario();
            controlinstitucion ci = new controlinstitucion();
            if (cu.contusuario() > 0 && ci.cont() > 0) {
                FrmLogin fr = new FrmLogin();
                fr.show();
            } else if (ci.cont() == 0) {
                frmParametrosInsti fp = new frmParametrosInsti();
                fp.show();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Datos Incorrectos");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

